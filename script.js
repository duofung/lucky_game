function getMemoryLevelConfig(level = memoryLevel) {
  let cells = 4;
  let targets = 1;

  if (level === 1) {
    cells = 4;
  } else if (level <= 4) {
    cells = 6;
  } else if (level <= 10) {
    cells = 6 + (level - 4) * 2;
  } else if (level <= 15) {
    cells = 18 + (level - 11) * 2;
    targets = 2;
  } else if (level <= 20) {
    cells = 28 + (level - 16) * 2;
    targets = 4;
  } else {
    cells = 38 + (level - 21) * 2;
    targets = 6;
  }

  return {
    level,
    cells,
    targets,
    columns: Math.min(6, Math.max(2, Math.ceil(Math.sqrt(cells)))),
  };
}

